package com.sevenwonders.game;

public class BlueCardInfo {

    private int id;
    private int age;
    private String name;
    private String texture;
    private int points;

    BlueCardInfo(int id, int age, String name, String texture, int points) {
        this.id = id;
        this.age = age;
        this.name = name;
        this.texture = texture;
        this.points = points;
    }
}
