package com.sevenwonders.game;

public class GreenCardInfo {

    private int id;
    private int age;
    private String name;
    private String texture;
    private int compass;
    private int gear;
    private int tablet;

    GreenCardInfo(int id, int age, String name, String texture, int compass, int gear, int tablet) {
        this.id = id;
        this.age = age;
        this.name = name;
        this.texture = texture;
        this.compass = compass;
        this.gear = gear;
        this.tablet = tablet;
    }
}
