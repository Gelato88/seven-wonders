package com.sevenwonders.game;

public class RedCardInfo {

    private int id;
    private int age;
    private String name;
    private String texture;
    private int military;

    RedCardInfo(int id, int age, String name, String texture, int military) {
        this.id = id;
        this.age = age;
        this.name = name;
        this.texture = texture;
        this.military = military;
    }
}
